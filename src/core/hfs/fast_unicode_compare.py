def fast_unicode_compare(str1: str, str2: str) -> int:
    """
    HFS+ FastUnicodeCompare 算法
    
    按 Apple TN1150 规范实现的快速 Unicode 比较算法。
    使用 lower case 表进行大小写折叠，忽略特定字符。
    
    Args:
        str1: 第一个字符串
        str2: 第二个字符串
    
    Returns:
        -1 (str1 < str2), 0 (相等), 1 (str1 > str2)
    """
    # 转换为 UTF-16 编码的字符数组
    chars1 = [ord(c) for c in str1]
    chars2 = [ord(c) for c in str2]
    
    len1 = len(chars1)
    len2 = len(chars2)
    ptr1 = 0
    ptr2 = 0
    
    table = _G_LOWER_CASE_TABLE
    
    while True:
        # 设置默认值（到达末尾）
        c1 = 0
        c2 = 0
        
        # 查找 str1 的下一个有效字符
        while len1 > 0 and c1 == 0:
            c1 = chars1[ptr1]
            ptr1 += 1
            len1 -= 1
            
            # 查找子表
            temp = table[c1 >> 8]
            if temp != 0:
                # 有子表，进行大小写折叠
                c1 = table[temp + (c1 & 0x00FF)]
        
        # 查找 str2 的下一个有效字符
        while len2 > 0 and c2 == 0:
            c2 = chars2[ptr2]
            ptr2 += 1
            len2 -= 1
            
            # 查找子表
            temp = table[c2 >> 8]
            if temp != 0:
                # 有子表，进行大小写折叠
                c2 = table[temp + (c2 & 0x00FF)]
        
        # 比较
        if c1 != c2:
            break
        
        # 都到达末尾
        if c1 == 0:
            return 0
    
    # 当 c1 != c2 时
    if c1 < c2:
        return -1
    else:
        return 1


# 测试函数
def _test_fast_unicode_compare():
    """测试 FastUnicodeCompare 算法"""
    test_cases = [
        ("", "", 0),
        ("a", "a", 0),
        ("a", "b", -1),
        ("b", "a", 1),
        ("A", "a", 0),  # 大小写不敏感
        ("a", "A", 0),  # 大小写不敏感
        ("abc", "ABC", 0),  # 大小写不敏感
        ("abc", "abd", -1),
        ("abd", "abc", 1),
        ("abc", "abc", 0),
        ("abc", "abcd", -1),
        ("abcd", "abc", 1),
    ]
    
    for s1, s2, expected in test_cases:
        result = fast_unicode_compare(s1, s2)
        status = "✓" if result == expected else "✗"
        print(f"{status} fast_unicode_compare({s1!r}, {s2!r}) = {result} (expected {expected})")


if __name__ == "__main__":
    _test_fast_unicode_compare()
